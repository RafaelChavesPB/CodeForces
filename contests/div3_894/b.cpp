void solve(){
	ll n; 
	cin >>n;
	vector<ll> arr(n), b; for( auto &it: arr) cin >> it;
	b.push_back(arr[0]);
	for(int i = 1; i < n; i++){
		if(arr[i-1] > arr[i]){
			b.push_back(1);
		}
		b.push_back(arr[i]);
	}
	cout << b.size() << '\n';
	for(auto it: b)
		cout << it << ' ';
	cout << endl;
}
 
