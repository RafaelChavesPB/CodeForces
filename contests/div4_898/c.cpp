void solve(){
	vector<string> arr(10); for(int i = 0; i < 10; i++) cin >> arr[i]; 
	ll ans = 0;
	for(int i = 0; i < 10; i++){
		for(int j = 0; j < 10; j++){
			if(arr[i][j] == 'X'){
				ans += min(i+1, min(j+1, min(10-i, 10-j)));		
			}
		}
	}
	cout << ans << '\n';
}
 
