#define all(v) v.begin(), v.end()
 
void solve(){
	ll n, k; 
	cin >>n >> k;
	vector<int> freq(26, 0);
	string s;
	int couples = 0, singles = 0;
	cin >> s;
	for(auto c: s)
		freq[c-'a']++;
	for(auto f: freq){
		couples += f/2;
		singles += f%2;
	}
	bug2(couples, singles);
	int ans = 2*(couples/k); 
	singles += 2*(couples%k);
	cout << ans + (k<= singles?1:0) << '\n';
}
 
int main(){
	ios_base::sync_with_stdio(false); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--)
		solve();
	return 0;
}
