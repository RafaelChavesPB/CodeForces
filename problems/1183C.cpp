void solve(){
	int n, k, a, b; 
	cin >> k >> n >> a >> b;
	int Bs = min(n, k/b);
	if(Bs < n or Bs*b == k){
		cout << -1 << '\n';
		return;
	}
	int As = min(n, (k - Bs*b)/(a-b));
	Bs -= As;
	if(As*a + Bs*b == k)
		As--;
	cout << As << '\n';
}
 
